class Calculadora{
    sumar(nun1,nun2){
        return nun1 + nun2;
    }
    restar(nun1,nun2){
        return nun1 - nun2;
    }
    multiplicacion(nun1,nun2){
        return nun1 * nun2;
    }
    division(nun1,nun2){
        return nun1 / nun2;
    }
    modulo(nun1,nun2){
        return nun1 % nun2;
    }
}